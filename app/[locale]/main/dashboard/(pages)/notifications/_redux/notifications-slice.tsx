export const notificationSlice = createSlice({
    name: "notification",
    initialState,
    reducers: {
        nextPage: (state: NotificationState) => {
            return {
                ...state,
                pagination: {
                    ...state.pagination,
                    currentPage: state.pagination.currentPage + 1
                }
            }
        },
        pageNumberPressed: (state: NotificationState, action: PayloadAction<number>) => {
            return {
                ...state,
                pagination: {
                    ...state.pagination,
                    currentPage: action.payload
                }
            }
        },
        previousPage: (state: NotificationState) => {
            return {
                ...state,
                pagination: {
                    ...state.pagination,
                    currentPage: state.pagination.currentPage - 1
                }
            }
        },
        notificationFilterDateChanged: (state: NotificationState, action: PayloadAction<any>) => {
            return {
                ...state,
                notificationFilterDateRange: {
                    ...state.notificationFilterDateRange,
                    value: action.payload,
                    error: ''
                }
            }
        },
    }
})

export const {
    notificationFilterDateChanged,
    nextPage, pageNumberPressed, previousPage
} = notificationSlice.actions

export default notificationSlice.reducer